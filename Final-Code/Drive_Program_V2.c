#include "JoystickDriver.c"

task liftPresets();

void init();
void driveChassis(bool t);
void driveRoller();

static float fullFactor = 0.781;
static float halfFactor = 0.391;

task main()
{
	//setup
	bool chassisToggle = false;
	init();
	startTask(liftPresets);
	//main drive
	while (true) {
		getJoystickSettings(joystick);
		if (joy1Btn(2)) {
			wait1Msec(200);
			chassisToggle = !chassisToggle;
			playImmediateTone((rand() % (800-300)) + 300, 5);
		}

		driveChassis(chassisToggle);
		driveRoller();

		if (joy1Btn(6)) {
			servo[gateServo] = 90;
		}
		if (joy1Btn(5)) {
			servo[gateServo] = 0;
		}

		delay(1);
	}
}

//inits encoders and servos
void init() {
	nMotorEncoder[liftL] = 0;
	nMotorEncoder[liftR] = 0;
	nMotorEncoder[chassisL] = 0;
	nMotorEncoder[chassisR] = 0;
	servo[gateServo] = 0;
}

//controls drive motors and determines speed toggle
void driveChassis(bool t) {
	if (t) {
		motor[chassisL] = -joystick.joy1_y1 * fullFactor;
		motor[chassisR] = joystick.joy1_y2 * fullFactor;
	} else {
		motor[chassisL] = -joystick.joy1_y1 * halfFactor;
		motor[chassisR] = joystick.joy1_y2 * halfFactor;
	}
}

//controls the roller
void driveRoller() {
	if (joy1Btn(8) || joy2Btn(8)) {
		motor[roller] = 100;
	} else if (joy1Btn(7) || joy2Btn(7)) {
		motor[roller] = -100;
	} else {
		motor[roller] = 0;
	}
}

//drive the lift, has 5 presets
task liftPresets() {
	while (true) {
		getJoystickSettings(joystick);
		//highest setting
		if (joy2Btn(3) && nMotorEncoder[liftL] > -5500) {
			while (nMotorEncoder[liftL] > -5500) {
				motor[liftL] = -100;
				motor[liftR] = -100;
			}
		}
		// medium-high setting
		if (joy2Btn(4) && nMotorEncoder[liftL] > -4000) {
			while (nMotorEncoder[liftL] > -4000) {
				motor[liftL] = -100;
				motor[liftR] = -100;
			}
		} else if (joy2Btn(4)) {
			while (nMotorEncoder[liftL] < -4000) {
				motor[liftL] = 25;
				motor[liftR] = 25;
			}
		}
		// medium-low setting
		if (joy2Btn(1) && nMotorEncoder[liftL] > -3000) {
			while (nMotorEncoder[liftL] > -3000) {
				motor[liftL] = -100;
				motor[liftR] = -100;
			}
		} else if (joy2Btn(1)) {
			while (nMotorEncoder[liftL] < -3000) {
				motor[liftL] = 25;
				motor[liftR] = 25;
			}
		}
		// low setting
		if (joy2Btn(2) && nMotorEncoder[liftL] > -1500) {
			while (nMotorEncoder[liftL] > -1500) {
				motor[liftL] = -100;
				motor[liftR] = -100;
			}
		} else if (joy2Btn(2)) {
			while (nMotorEncoder[liftL] < -1500) {
				motor[liftL] = 25;
				motor[liftR] = 25;
			}
		}
		// ground setting
		if (joystick.joy2_TopHat == 0) {
			while (nMotorEncoder[liftL] < 0) {
				motor[liftL] = 25;
				motor[liftR] = 25;
			}
		}
		delay(1);
	}
}
